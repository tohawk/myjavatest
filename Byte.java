public class Byte{
	public static void main(String[] args){
		byte x = 22;
		byte y = 022;
		byte z = 0x22;
		float a = 22.5f;
		float b = 50.1f;
		float ab = a*b;
		System.out.println("trun to ten, x="+x);
		System.out.println("trun to ten, y="+y);
		System.out.println("trun to ten, z="+z);
		System.out.println("trun to ten, a="+a);
		System.out.println("trun to ten, b="+b);
		System.out.println("trun to ten, ab="+ab);
	}
}

